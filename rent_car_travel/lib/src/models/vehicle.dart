  final int id;
  final String nameCar;
  final String imageCar;
  final String categoryID;
  final String mode;
  final int numberOfSeats;
  final String licensePlates;
  final String status;

  Vehicle(
      {this.id,
      this.nameCar,
      this.imageCar,
      this.categoryID,
      this.mode,
      this.numberOfSeats,
      this.licensePlates,
      this.status});
}
